package dailyAssignments;

public class Assignment2 {
	public static void main(String[] args) {
		//Assignment 1: ASCII Code Program
		
		char ch1 = 'A';
		int n1 = ch1;
		System.out.println("Aski value of A: "+n1);
		
		char ch2 = 'a';
		int n2 = ch2;
		System.out.println("Aski value of a: "+n2);
		
		char ch3 = '0';
		int n3 = ch3;
		System.out.println("Aski value of 0: "+n3);
		
		char ch4 = '@';
		int n4 = ch4;
		System.out.println("Aski value of @: "+n4);
		
		
		
		//Assignment 2: Print Triangle using println()
		System.out.println();
		System.out.println("*");
		System.out.println("**");
		System.out.println("***");
		System.out.println("****");
		System.out.println("*****");
		
		
		//Assignment 3: Type Casting
		
		double doubleNumber = 10.75d;
		
		System.out.println("Double number: "+doubleNumber);
		
		int num =(int)doubleNumber;
		System.out.println("Converted int num: "+num);
	}

}
